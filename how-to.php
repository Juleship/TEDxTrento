<h2>• HOMEPAGE</h2>
<p>E' modificabile solo la parte centrale accanto al video, può essere modificata andando su <strong>Posts / Articoli</strong>, e modificando l'articolo chiamato <strong>homepage-paragraph</strong>.</p>

<hr />

<h2>• ABOUT</h2>
<p>Per modificare il contenuto di ogni sottosezione di <strong>about</strong>, accedere a <strong>Posts / Articoli</strong> e modificare gli articoli chiamati <strong>about-ted</strong>, <strong>about-tedx</strong> e <strong>about-tedxtrento</strong>.</p>

<hr />

<h2>• SPEAKERS</h2>
<p>
	Per modificare il paragrafo in fondo alla pagina Speakers si deve modificare il contenuto del <strong>Post</strong> chiamato <strong>Speakers-intro</strong>.
</p>
<p>Per la gestione degli speakers utilizzare il plugin <strong>Site Members</strong> nel menu a sinistra:</p>
<ul>
	<li>- Per inserire un nuovo speaker, fare click su <strong>Add New</strong> e inserire <strong>Nome</strong>, <strong>Staff Photo</strong>, <strong>Position</strong> (TITOLO DELLA TALK), <strong>Facebook URL</strong> (SITO WEB, solo www.example.com senza http:// ) , <strong>Staff Member Bio</strong> (ARTICOLO CONTENENTE TUTTE LE INFORMAZIONI DELLA TALK) ed <strong>Excerpt</strong> (BIOGRAFIA NON PIU LUNGA DI 140 CARATTERI). Dopodiché spuntare in basso a destra la casella <strong>Speakers</strong> e premere <strong>Pubblica</strong>. IMPORTANTE: la fotografia deve essere quadrata (non più di 300x300 pixels).</li> 
	<li>- Per visualizzare la lista degli speakers finora aggiunti premere su <strong>All Staff Members</strong> (nota bene: Speakers, Team e Partners sono tutti nella stessa lista, l'ordine che apparirà nel sito è relativo solo al gruppo visualizzato - es: solo a Speakers).</li>
	<li>- Per modificare uno speakers accedere ad <strong>All Staff Members</strong> e premere su <strong>Edit</strong> sotto alla persona che si vuole modificare.</li>
</ul>
</p>

<hr />

<h2>• TEAM</h2>
<p>
	Per modificare il paragrafo in intestazione a Team si deve modificare il contenuto del <strong>Post</strong> chiamato <strong>Team-intro</strong>.
	<br />
	Per modificare il paragrafo in fondo alla pagina Team si deve modificare il contenuto del <strong>Post</strong> chiamato <strong>Team-outro</strong>.
</p>
<p>Per la gestione del team, utilizzare il plugin <strong>Site Members</strong> nel menu a sinistra:</p>
<ul>
	<li>- Per inserire un nuovo membro del team, fare click su <strong>Add New</strong> e inserire <strong>Nome</strong>, <strong>Staff Photo</strong> e <strong>Position</strong>. Dopodiché spuntare in basso a destra la casella <strong>Team</strong> e premere <strong>Pubblica</strong>. IMPORTANTE: la fotografia deve essere quadrata (non più di 300x300 pixels) e avere lo sfondo bianco!</li> 
	<li>- Per visualizzare la lista dei membri finora aggiunti premere su <strong>All Staff Members</strong> (nota bene: Speakers, Team e Partners sono tutti nella stessa lista, l'ordine che apparirà nel sito è relativo solo al gruppo visualizzato - es: solo a Team).</li>
	<li>- Per modificare un membro del Team accedere ad <strong>All Staff Members</strong> e premere su <strong>Edit</strong> sotto alla persona che si vuole modificare.</li>
</ul>

